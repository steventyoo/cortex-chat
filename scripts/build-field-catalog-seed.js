#!/usr/bin/env node

/**
 * Reads data/taxonomy.json + data/field-canonical-map.json and generates
 * a SQL migration that seeds field_catalog and skill_fields from the taxonomy.
 *
 * Usage:  node scripts/build-field-catalog-seed.js
 * Output: supabase/migrations/20260419_seed_field_catalog.sql
 */

const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const taxonomy = JSON.parse(fs.readFileSync(path.join(ROOT, 'data/taxonomy.json'), 'utf8'));
const canonicalMap = JSON.parse(fs.readFileSync(path.join(ROOT, 'data/field-canonical-map.json'), 'utf8'));

function esc(s) {
  if (s == null) return 'NULL';
  return "'" + String(s).replace(/'/g, "''") + "'";
}

function autoCanonical(name) {
  return name
    .replace(/[^a-zA-Z0-9\s]/g, '')
    .replace(/\s+/g, '_')
    .toLowerCase()
    .replace(/_+/g, '_')
    .replace(/^_|_$/g, '');
}

function inferFieldType(attr) {
  const t = (attr.type || 'string').toLowerCase();
  if (['string', 'number', 'date', 'enum', 'boolean', 'array'].includes(t)) return t;
  return 'string';
}

function inferCategory(canonical, attr) {
  const c = canonical.toLowerCase();
  if (/^(project|document_id|report_id|co_number|rfi_number|submittal_number|asi_number|ccd_number|job_number|estimate_id|contract_id|bid_package_id|activity_record_id|meeting_id|punch_list_id|pay_app_number|correspondence_id|originating|triggering|resulting|related|cross_ref)/.test(c)) return 'identity';
  if (/cost|amount|budget|price|bid_amount|markup|fee|revenue|margin|subcontract|contract_value|retainage|billed|payment|billing|savings|delta|buyout/.test(c)) return 'financial';
  if (/date|schedule|period|days_|pipeline_duration|forecast|time|deadline/.test(c)) return 'schedule';
  if (/csi|spec|division|uniformat|trade|building_system|location|scope|drawing|quantity|unit_of_measure|production|productivity|labor|material|equipment|work_phase|activity_type|rate|ratio|efficiency/.test(c)) return 'technical';
  if (/root_cause|preventability|recurring|severity|hazard|safety|rework|disruption|risk|failure|corrective|inspection|weather|delay_cause|punch_list|lessons|disputed|quality|deviation|variance_root/.test(c)) return 'quality';
  return 'admin';
}

// Phase 1: Build canonical catalog entries
const catalogEntries = new Map(); // canonical_name -> { display_name, field_type, category, description }

for (const skill of taxonomy) {
  for (const attr of skill.attributes) {
    const canonical = canonicalMap[attr.name] || autoCanonical(attr.name);

    if (!catalogEntries.has(canonical)) {
      const fieldType = inferFieldType(attr);
      const category = inferCategory(canonical, attr);
      const displayName = attr.name
        .replace(/ \(.*\)$/, '')
        .replace(/ — .*$/, '')
        .replace(/ \/ .*$/, '')
        .replace(/ \(if .*$/, '');

      catalogEntries.set(canonical, {
        display_name: displayName,
        field_type: fieldType,
        category: category,
        description: (attr.description || '').split('.')[0] || '',
      });
    }
  }
}

// Phase 2: Build skill_fields rows
const skillFieldRows = [];
for (const skill of taxonomy) {
  let sortIdx = 0;
  for (const attr of skill.attributes) {
    sortIdx++;
    const canonical = canonicalMap[attr.name] || autoCanonical(attr.name);
    const displayOverride = attr.name;
    const tier = attr.tier != null ? attr.tier : 1;
    const required = attr.required ? true : false;
    const importance = attr.importance || null;
    const description = attr.description || '';
    const options = attr.options && attr.options.length > 0 ? attr.options : null;
    const example = '';
    const extractionHint = attr.llmTechnique || attr.disambiguationRules || '';
    const disambiguationRules = attr.disambiguationRules || '';

    skillFieldRows.push({
      skill_id: skill.skillId,
      canonical,
      display_override: displayOverride,
      tier,
      required,
      importance,
      description,
      options,
      example,
      extraction_hint: extractionHint,
      disambiguation_rules: disambiguationRules,
      sort_order: sortIdx,
    });
  }
}

// Phase 3: Emit SQL migration
const lines = [];
lines.push('-- Auto-generated by scripts/build-field-catalog-seed.js');
lines.push('-- Derived from data/taxonomy.json + data/field-canonical-map.json');
lines.push('-- This replaces the guessed seed with taxonomy-derived data.');
lines.push('');
lines.push('BEGIN;');
lines.push('');
lines.push('-- Clean slate: remove old guessed data');
lines.push('DELETE FROM skill_fields;');
lines.push('DELETE FROM field_catalog;');
lines.push('');
lines.push('-- ============================================================');
lines.push('-- field_catalog: one row per canonical concept');
lines.push('-- ============================================================');
lines.push('');

const catalogArr = [...catalogEntries.entries()].sort((a, b) => a[0].localeCompare(b[0]));
for (const [canonical, info] of catalogArr) {
  lines.push(
    `INSERT INTO field_catalog (canonical_name, display_name, field_type, category, description) VALUES (${esc(canonical)}, ${esc(info.display_name)}, ${esc(info.field_type)}, ${esc(info.category)}, ${esc(info.description)}) ON CONFLICT (canonical_name) DO NOTHING;`
  );
}

lines.push('');
lines.push('-- ============================================================');
lines.push('-- skill_fields: one row per (skill, field) pair');
lines.push('-- ============================================================');
lines.push('');

for (const row of skillFieldRows) {
  const optionsVal = row.options ? esc(JSON.stringify(row.options)) : 'NULL';
  lines.push(
    `INSERT INTO skill_fields (skill_id, field_id, display_override, tier, required, importance, description, options, example, extraction_hint, disambiguation_rules, sort_order) VALUES (${esc(row.skill_id)}, (SELECT id FROM field_catalog WHERE canonical_name = ${esc(row.canonical)}), ${esc(row.display_override)}, ${row.tier}, ${row.required}, ${row.importance ? esc(row.importance) : 'NULL'}, ${esc(row.description)}, ${optionsVal}::jsonb, ${esc(row.example)}, ${esc(row.extraction_hint)}, ${esc(row.disambiguation_rules)}, ${row.sort_order}) ON CONFLICT (skill_id, field_id) DO NOTHING;`
  );
}

lines.push('');
lines.push('COMMIT;');
lines.push('');

const outPath = path.join(ROOT, 'supabase/migrations/20260419_seed_field_catalog.sql');
fs.writeFileSync(outPath, lines.join('\n'));
console.log(`✓ Wrote ${outPath}`);
console.log(`  ${catalogArr.length} catalog entries, ${skillFieldRows.length} skill_fields rows`);
